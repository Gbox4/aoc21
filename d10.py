pairs = {
    "(": ")",
    "{": "}",
    "[": "]",
    "<": ">"
}
complete_strs = []
for line in data:
    checked = []
    incomplete = True
    for c in line:
        if c in "([{<":
            checked.append(c)
        else:
            if c == pairs[checked[-1]]:
                checked.pop(-1)
            else:
                total += scores[c]
                incomplete = False
                break
    if incomplete:
        row = ""
        for i in reversed(checked):
            row += pairs[i]
        complete_strs.append(row)

print(total)
    
